#include <bits/stdc++.h>

using namespace std;



int main(){
    int n;
    vector<long long int> b(50);
    b[0]=1;
    b[1]=2;

    for(int i=2 ; i<=50 ; i++){
        b[i] = b[i-1] + b[i-2];
    }


    while(cin>>n && n!=-1){

        if(n==-1){
            break;
        }
        if(n==0){
            cout<<1<<" "<<0<<'\n';
        }else if(n==1){
            cout<<2<<" "<<1<<'\n';
        }else{
            cout<<b[n*2+1-2]<<" "<<b[n*2-2]<<'\n';
        }

    }
}
